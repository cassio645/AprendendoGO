/*
- Escreva expressões utilizando os operadores, e atribua seus valores a variáveis.
- Demonstre estes valores. > >= < <= =
*/

package main

import "fmt"

func main() {

	// Criando expressões e atribuindo a variaveis
	maiorIgual := 10 >= 20
	maior := 10 > 20
	igual := 10 == 20
	diferente := 10 != 20
	menorIgual := 10 <= 20
	menor := 10 < 20

	fmt.Println(maiorIgual)
	fmt.Println(maior)
	fmt.Println(igual)
	fmt.Println(diferente)
	fmt.Println(menorIgual)
	fmt.Println(menor)

}
